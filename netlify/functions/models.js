const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  }
});

const blogSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true
  },
  content: {
    type: String,
    required: true
  },
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true
  },
  author: {
    type: String,
    required: true
  }
}, { timestamps: true });

module.exports = {
  User: mongoose.model("User", userSchema),
  Blog: mongoose.model("Blog", blogSchema)
};